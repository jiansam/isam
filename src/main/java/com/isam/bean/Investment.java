package com.isam.bean;

public class Investment {
	private String investNo;
	private String oInvestNo;
	private String compName;
	
	public String getInvestNo() {
		return investNo;
	}
	public void setInvestNo(String investNo) {
		this.investNo = investNo;
	}
	public String getoInvestNo() {
		return oInvestNo;
	}
	public void setoInvestNo(String oInvestNo) {
		this.oInvestNo = oInvestNo;
	}
	public String getCompName() {
		return compName;
	}
	public void setCompName(String compName) {
		this.compName = compName;
	}
}
